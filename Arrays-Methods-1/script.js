"use Strict";

let fruits=["Apple","Banana"]

console.log(fruits);

//Push
fruits.push("Mango","Grapes")
console.log(fruits)
//Pop
let removedele=fruits.pop()
console.log(removedele)
console.log(fruits)
//Shift
let removed1=fruits.shift()
console.log(removed1)
console.log(fruits)

//Unshift
fruits.unshift("Orange","Pineapple",1,true)
console.log(fruits)
